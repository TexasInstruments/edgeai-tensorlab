import os
import sys
import argparse
import warnings

from edgeai_benchmark import *


def get_arg_parser():
    parser = argparse.ArgumentParser()
    parser.add_argument('settings_file', type=str, default=None)
    parser.add_argument('--dataset_name', type=str, nargs='*')
    return parser


if __name__ == '__main__':
    print(f'argv: {sys.argv}')
    # the cwd must be the root of the respository
    if os.path.split(os.getcwd())[-1] == 'scripts':
        os.chdir('../')
    #

    parser = get_arg_parser()
    args = parser.parse_args()

    assert args.dataset_name is not None, f'dataset_name must be provided: {args.dataset_name}'

    kwargs = vars(args)
    settings_file = kwargs.pop('settings_file')
    dataset_name = kwargs.pop('dataset_name')

    settings = config_settings.ConfigSettings(settings_file, **kwargs)

    interfaces.run_download_dataset(settings, dataset_name=dataset_name)
