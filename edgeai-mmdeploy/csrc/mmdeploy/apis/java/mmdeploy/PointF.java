package mmdeploy;

/** @description: the PointF class. */
public class PointF {

    /** x coordinate. */
    public float x;

    /** y coordinate. */
    public float y;

    /** Initialize a new instance of the PointF class.
     * @param x: x coordinate.
     * @param y: y coordinate.
    */
    public PointF(float x, float y) {
        this.x = x;
        this.y = y;
    }
}
