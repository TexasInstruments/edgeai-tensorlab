def get_config():
    model_config = xnn.utils.ConfigNode()
    model_config.input_channels = 3
    model_config.num_classes = 1000
    model_config.width_mult = 1.0
    model_config.strides = None #(2,2,2,2,2)
    model_config.fastdown = False
    model_config.enable_fp16 = False
    return model_config


def resnet50_with_model_config(model_config, pretrained=None):
    model_config = get_config().merge_from(model_config)
    model = resnet50(input_channels=model_config.input_channels, strides=model_config.strides,
                     num_classes=model_config.num_classes, pretrained=pretrained,
                     width_mult=model_config.width_mult, fastdown=model_config.fastdown,
                     enable_fp16=model_config.enable_fp16)
    return model


def resnet18_with_model_config(model_config, pretrained=None):
    model_config = get_config().merge_from(model_config)
    model = resnet18(input_channels=model_config.input_channels, strides=model_config.strides,
                     num_classes=model_config.num_classes, pretrained=pretrained,
                     width_mult=model_config.width_mult, fastdown=model_config.fastdown,
                     enable_fp16=model_config.enable_fp16)
    return model

